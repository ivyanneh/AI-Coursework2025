# Sum and average of 5 numbers

a = int(input("Enter first number: "))
b = int(input("Enter second number: "))
c = int(input("Enter third number: "))
d = int(input("Enter fourth number: "))
e = int(input("Enter fifth number: "))

sum = a + b + c + d + e
avg = sum / 5

print("Sum =", sum)
print("Average =", avg)
